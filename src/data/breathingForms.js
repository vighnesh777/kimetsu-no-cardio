// Ordinal helpers
const ORDINALS = ['','First','Second','Third','Fourth','Fifth','Sixth','Seventh',
  'Eighth','Ninth','Tenth','Eleventh','Twelfth','Thirteenth','Fourteenth',
  'Fifteenth','Sixteenth'];

export function getOrdinal(n) {
  return ORDINALS[n] || `${n}th`;
}

// How many forms are unlocked given total workout count
// One new form every 5 workouts
export function getUnlockedFormCount(totalWorkouts) {
  return Math.min(1 + Math.floor(totalWorkouts / 5), 999);
}

export function getFormsForStyle(styleId) {
  return BREATHING_FORMS[styleId] || [];
}
